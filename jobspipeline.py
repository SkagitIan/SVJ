def seed_businesses(queries):
    conn = sqlite3.connect('skagit_jobs.db')
    cursor = conn.cursor()

    for query in queries:
        print(f"🔍 Searching Outscraper for: {query}...")
        results = out_client.google_maps_search(query, limit=20, language='en')

        # Outscraper returns a list of results (one per query)
        # We grab the first list of places
        places = results[0] if results else []

        for place in places:
            name = place.get('name')
            # FIX: Outscraper uses 'website', your old code used 'site'
            site = place.get('website')
            city = place.get('city')

            if site:
                print(f"  ✅ Saving: {name} ({site})")
                cursor.execute('''
                    INSERT OR IGNORE INTO businesses (name, city, homepage_url)
                    VALUES (?, ?, ?)
                ''', (name, city, site))
            else:
                print(f"  ⚠️ Skipping {name}: No website found.")

    conn.commit()
    conn.close()
    print("\n🏁 Discovery Phase Complete. Run the 'Scout' cell next.")

