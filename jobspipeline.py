def run_delta_crawler():
    conn = sqlite3.connect('skagit_jobs.db')
    cursor = conn.cursor()
    cursor.execute('SELECT id, careers_url, last_hash FROM businesses')
    rows = cursor.fetchall()

    h = html2text.HTML2Text()
    h.ignore_links = False

    for b_id, url, old_hash in rows:
        try:
            print(f"Crawling: {url}")
            # In Production: Call Cloudflare Browser Rendering API
            # For Colab: Standard request
            resp = requests.get(url, timeout=15)
            markdown_content = h.handle(resp.text)

            new_hash = hashlib.sha256(markdown_content.encode()).hexdigest()

            if new_hash != old_hash:
                print(f" ✨ Change detected for Business ID {b_id}!")
                # Simulate R2 Upload
                with open(f"change_{b_id}.md", "w") as f:
                    f.write(markdown_content)

                cursor.execute('UPDATE businesses SET last_hash = ? WHERE id = ?', (new_hash, b_id))
                # Trigger extraction
                extract_jobs_from_markdown(b_id, markdown_content)
        except Exception as e:
            print(f"Error crawling {url}: {e}")

    conn.commit()
    conn.close()

def extract_jobs_from_markdown(business_id, markdown_text):
    # This tells Gemini exactly how to format the data
    prompt = f"""
    Extract all active job postings from the following markdown text.
    Return a list of objects with "job_title", "salary", and "apply_link".

    TEXT:
    {markdown_text[:5000]}
    """

    # Use 'response_mime_type' to force clean JSON output
    response = model.generate_content(
        prompt,
        generation_config={"response_mime_type": "application/json"}
    )

    try:
        # No more .replace() needed!
        jobs = json.loads(response.text)

        print(f"✅ Found {len(jobs)} jobs for Business {business_id}")
        for job in jobs:
            print(f" - {job.get('job_title', 'N/A')}")

    except Exception as e:
        print(f"❌ Extraction error: {e}")

# This triggers the crawl, hash check, and Gemini extraction
run_delta_crawler()

